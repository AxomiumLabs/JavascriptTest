
function greater(num){
    
    if (num>10){
        return true;
    }
    return false;
}
console.log(greater(12));


//equality operator
function equals(num){
    
    if (num==2){
        return true;
    }
    return false;
}
console.log(equals(12));

//inequality operator
function inequals(a,b){
    
    if (a!=b){
        return "not equals";
    }
    return "equals";
}
console.log(inequals(12,2));

